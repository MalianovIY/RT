#include "include/libgm.h"

t_float4	gm_vec_mat_mult(t_float4 v1, t_mat4 t)
{
	int			i;
	t_float4	v;

	i = -1;
	while (++i < 4)
		v.v[i] = v1.v[0] * t.v[0].v[i] + v1.v[1] * t.v[1].v[i]
				+ v1.v[2] * t.v[2].v[i] + v1.v[3] * t.v[3].v[i];
	return (v);
}
