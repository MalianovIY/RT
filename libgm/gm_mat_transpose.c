#include "include/libgm.h"

t_mat4	gm_mat_transpose(t_mat4 t)
{
	int		i[2];
	t_mat4	t0;

	t0 = t;
	i[0] = 0;
	while (++i[0] < 4)
	{
		i[1] = i[0];
		while (++i[1] < 4)
			ft_memswap((void *)&(t0.v[i[0]].v[i[1]]),
					(void *)&(t0.v[i[1]].v[i[0]]), sizeof(float));
	}
	return (t0);
}
