#include "include/libgm.h"

t_float4	gm_vec_add(t_float4 v1, t_float4 v2)
{
	int			i;
	t_float4	v;

	i = -1;
	while (++i < 3)
		v.v[i] = v2.v[i] + v1.v[i];
	v.v[3] = (!(v1.v[3]) || (v2.v[3])) ? 0 : 1;
	return (v);
}
