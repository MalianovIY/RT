#include "include/libgm.h"

t_int4	gm_init_int(int x, int y, int z, int w)
{
	t_int4	f;

	f.v[0] = x;
	f.v[1] = y;
	f.v[2] = z;
	f.v[3] = w;
	return (f);
}
