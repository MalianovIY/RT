#include "include/libgm.h"

float	gm_vec_dot(t_float4 v, t_float4 v1)
{
	float	s;
	int		i;

	s = 0;
	i = -1;
	while (++i < 3)
		s += v.v[i] * v1.v[i];
	return (s);
}
