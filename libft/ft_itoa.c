#include "includes/libft.h"

char	*ft_itoa(int n)
{
	long long int	x[3];
	char			s[12];
	char			*o;

	x[2] = n;
	if ((x[1] = x[2]) < 0)
		x[2] = -x[2];
	x[0] = 0;
	s[x[0]++] = x[2] % 10 + '0';
	while ((x[2] /= 10) > 0)
		s[x[0]++] = x[2] % 10 + '0';
	if (x[1] < 0)
		s[x[0]++] = '-';
	s[x[0]] = '\0';
	o = ft_strsub(s, 0, x[0]);
	if (o == NULL)
		return (NULL);
	ft_strrev(o);
	return (o);
}
