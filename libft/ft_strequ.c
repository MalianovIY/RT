#include "includes/libft.h"

int	ft_strequ(const char *s1, const char *s2)
{
	unsigned char	*c1;
	unsigned char	*c2;

	c1 = (unsigned char *)s1;
	c2 = (unsigned char *)s2;
	if (c1 == NULL || c2 == NULL)
		return (0);
	while (*c1 && *c2)
		if (*c1++ != *c2++)
			return (0);
	if ((*c1 && !(*c2)) || (!(*c1) && *c2))
		return (0);
	return (1);
}
