#include "includes/libft.h"

void	ft_striteri(char *a, void (*f)(unsigned int i, char *))
{
	unsigned int	i;

	if (a == NULL || f == NULL)
		return ;
	i = 0;
	while (*a)
		f(i++, a++);
}
