#include "includes/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long int	x[3];
	char			s[12];

	x[2] = n;
	if ((x[1] = x[2]) < 0)
		x[2] = -x[2];
	x[0] = 0;
	s[x[0]] = x[2] % 10 + '0';
	while ((x[2] /= 10) > 0)
		s[++x[0]] = x[2] % 10 + '0';
	s[++x[0]] = x[2] % 10 + '0';
	if (x[1] < 0)
		s[x[0]++] = '-';
	s[x[0]] = '\0';
	ft_putstr_fd(ft_strrev(s), fd);
}
